    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String submit = request.getParameter("submit");
        if (submit != null && submit.equals("ADD")) {
            String proname = request.getParameter("proname");
            String proprice = request.getParameter("proprice");
            String proamount = request.getParameter("proamount");
            String prosupplier = request.getParameter("prosupplier");
            String proexpire = request.getParameter("proexpire");

            try {
                int price = Integer.parseInt(proprice);
                
                ProductDAO cdb = new ProductDAO();
                HttpSession session = request.getSession(false);
                double price1 = Double.parseDouble(proprice);
                int amount = Integer.parseInt(proamount);
                
                Product q = new Product(proname, price1, amount, prosupplier,  new java.sql.Date((new Date()).getTime()));
                Date ex = new java.sql.Date((new Date()).getTime());
                cdb.addProduct(q, ex);
                
                List<Product> p = cdb.getAllProduct();
                request.setAttribute("myproc", p);
                
                response.sendRedirect("addNewProduct");
            } catch (NumberFormatException e) {
                System.out.println(e);
            }
        }
    }

